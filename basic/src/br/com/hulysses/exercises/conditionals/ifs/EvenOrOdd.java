package br.com.hulysses.exercises.conditionals.ifs;

public class EvenOrOdd {

    public static void main(String[] args) {
        int number = 10;

        if (number % 2 == 0) {
            System.out.println(number + " is even.");
        } else {
            System.out.println(number + " is odd.");
        }
    }
}
